/**
 * Get passport color scheme based on nationality
 */
export function getPassportColorScheme(nationality: string): PassportColorScheme {
  const colorKey = NATIONALITY_COLORS[nationality] || 'burgundy'; // Default to burgundy
  return PASSPORT_SCHEMES[colorKey];
}

/**
 * Get passport gradient classes for Tailwind
 */
export function getPassportGradient(nationality: string): string {
  const scheme = getPassportColorScheme(nationality);
  return `bg-gradient-to-br ${scheme.from} ${scheme.via} ${scheme.to}`;
}

